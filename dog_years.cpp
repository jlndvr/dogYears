#include <iostream>

int main() {
  // My dog is 2
  int dog_age = 3;
  int early_years, later_years, human_years;
  // Set to 21
  early_years = 21;
  // Equation
  later_years = (dog_age - 2) * 4;
  human_years = early_years + later_years;
  std::cout<< "My name is Smokey! Ruff ruff, I am " << human_years << " years old in human years.\n";
}
